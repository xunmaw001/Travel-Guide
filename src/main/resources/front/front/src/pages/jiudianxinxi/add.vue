<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            jiudianmingcheng : false,
            jiudianleixing : false,
            jiudiandizhi : false,
            fangjianleixing : false,
            yuyuejiage : false,
            lianxidianhua : false,
            jiudianjieshao : false,
            jiudiantupian : false,
            thumbsupnum : false,
            crazilynum : false,
            clicktime : false,
            clicknum : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          jiudianmingcheng: '',
          jiudianleixing: '',
          jiudiandizhi: '',
          fangjianleixing: '',
          yuyuejiage: '',
          lianxidianhua: '',
          jiudianjieshao: '',
          jiudiantupian: '',
          thumbsupnum: '',
          crazilynum: '',
          clicktime: '',
          clicknum: '',
        },
        jiudianleixingOptions: [],
        fangjianleixingOptions: [],
        rules: {
          jiudianmingcheng: [
            { required: true, message: '酒店名称不能为空', trigger: 'blur' },
          ],
          jiudianleixing: [
            { required: true, message: '酒店类型不能为空', trigger: 'blur' },
          ],
          jiudiandizhi: [
          ],
          fangjianleixing: [
          ],
          yuyuejiage: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          lianxidianhua: [
            { validator: this.$validate.isMobile, trigger: 'blur' },
          ],
          jiudianjieshao: [
          ],
          jiudiantupian: [
          ],
          thumbsupnum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          crazilynum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          clicktime: [
          ],
          clicknum: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='jiudianmingcheng'){
              this.ruleForm.jiudianmingcheng = obj[o];
              this.ro.jiudianmingcheng = true;
              continue;
            }
            if(o=='jiudianleixing'){
              this.ruleForm.jiudianleixing = obj[o];
              this.ro.jiudianleixing = true;
              continue;
            }
            if(o=='jiudiandizhi'){
              this.ruleForm.jiudiandizhi = obj[o];
              this.ro.jiudiandizhi = true;
              continue;
            }
            if(o=='fangjianleixing'){
              this.ruleForm.fangjianleixing = obj[o];
              this.ro.fangjianleixing = true;
              continue;
            }
            if(o=='yuyuejiage'){
              this.ruleForm.yuyuejiage = obj[o];
              this.ro.yuyuejiage = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='jiudianjieshao'){
              this.ruleForm.jiudianjieshao = obj[o];
              this.ro.jiudianjieshao = true;
              continue;
            }
            if(o=='jiudiantupian'){
              this.ruleForm.jiudiantupian = obj[o].split(",")[0];
              this.ro.jiudiantupian = true;
              continue;
            }
            if(o=='thumbsupnum'){
              this.ruleForm.thumbsupnum = obj[o];
              this.ro.thumbsupnum = true;
              continue;
            }
            if(o=='crazilynum'){
              this.ruleForm.crazilynum = obj[o];
              this.ro.crazilynum = true;
              continue;
            }
            if(o=='clicktime'){
              this.ruleForm.clicktime = obj[o];
              this.ro.clicktime = true;
              continue;
            }
            if(o=='clicknum'){
              this.ruleForm.clicknum = obj[o];
              this.ro.clicknum = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.jiudianleixingOptions = "民宿,连锁酒店,快捷酒店,豪华酒店".split(',')
        this.fangjianleixingOptions = "单人房,大床房,双人间,三人间,标准套间,豪华套间".split(',')
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('jiudianxinxi/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('jiudianxinxi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('jiudianxinxi/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('jiudianxinxi/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      jiudiantupianUploadChange(fileUrls) {
          this.ruleForm.jiudiantupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

