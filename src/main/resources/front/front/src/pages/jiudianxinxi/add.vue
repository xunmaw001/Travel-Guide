            <el-input v-model="ruleForm.lianxidianhua" 
                placeholder="联系电话" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"10px","margin":"0 0 0px","display":"inline-block"}' label="酒店图片" v-if="type!='cross' || (type=='cross' && !ro.jiudiantupian)" prop="jiudiantupian">
